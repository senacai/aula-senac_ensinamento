for i in range ("Digite um numero"):
    j = 2
    counter = 0
    while j < ("numero variado"):
        if i % j == 0:
            counter = 1
            j += 1
        else:
            j += 1
    if counter == 0:
       print("resposta determinada")
       counter = ("numero final")
    else:
        counter = ("resultado")